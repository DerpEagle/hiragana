async def generate(text, path):
    """Generate a single audio file."""
    comm = edge_tts.Communicate(text, VOICE, rate="-10%")
    await comm.save(path)

async def main():
    os.makedirs(OUTPUT_DIR, exist_ok=True)

    all_texts = HIRAGANA + KATAKANA + WORDS_H + WORDS_K
    total = len(all_texts)
    done = 0
    skipped = 0

    for text in all_texts:
        path = os.path.join(OUTPUT_DIR, f"{text}.mp3")
        if os.path.exists(path):
            skipped += 1
            done += 1
            continue
        try:
            await generate(text, path)
            done += 1
            if done % 20 == 0:
                print(f"  {done}/{total} done...")
        except Exception as e:
            print(f"  ERROR: {text} — {e}")
            done += 1

    print(f"Done! {done} total, {skipped} skipped (already existed)")

if __name__ == "__main__":
    asyncio.run(main())
